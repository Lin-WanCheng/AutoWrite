
package wc;

public class POJOTest {
    /** 用户名 */
    private String user;
    /** 年龄 */
    private int age;

    /** 获取用户名 */
    public String getUser() {
        return user;
    }

    /** 设置用户名 */
    public void setUser(String user) {
        this.user = user;
    }

    /** 获取年龄 */
    public int getAge() {
        return age;
    }

    /** 设置年龄 */
    public void setAge(int age) {
        this.age = age;
    }
}
